template <class T>
T ext_gcd(T a, T b, T& x, T& y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    auto d = ext_gcd(b, a % b, y, x);
    y -= a / b * x;
    return d;
}

